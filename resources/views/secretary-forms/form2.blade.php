        නම් එහි අධ්‍යක්ෂ කෙනෙක් හෝ ලේකම් කෙනෙකු විසින් ද, ව්‍යාපාරික ආයතනයක් සම්බන්ධයෙන් නම් එහි හවුල්කරුවන් වූ
